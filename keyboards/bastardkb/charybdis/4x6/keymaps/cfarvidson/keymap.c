#define SHIFT_TAPPING_TERM 135
#define ALT_TAPPING_TERM 300


uint16_t get_tapping_term(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
        case HC_A:
            // Command
            return ALT_TAPPING_TERM;
        case HC_S:
            // Alt
            return ALT_TAPPING_TERM;
        case HC_L:
            // Alt
            return ALT_TAPPING_TERM;
        case HC_F:
            // Shift
            return SHIFT_TAPPING_TERM;
        case HC_J:
            // Shift
            return SHIFT_TAPPING_TERM;
        // case CC_Q:
        //     // Custom Q
        //     return ALT_TAPPING_TERM;
        default:
            return TAPPING_TERM;
    }
}


